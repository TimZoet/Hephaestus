#include "hephaestus/data_sources/bool_data_source.h"

////////////////////////////////////////////////////////////////
// Constructors.
////////////////////////////////////////////////////////////////

BoolDataSource::BoolDataSource(bool& v) : value(&v) {}

BoolDataSource::~BoolDataSource() noexcept = default;

////////////////////////////////////////////////////////////////
// Getters.
////////////////////////////////////////////////////////////////

bool BoolDataSource::get() { return *value; }

////////////////////////////////////////////////////////////////
// Setters.
////////////////////////////////////////////////////////////////

void BoolDataSource::set(const bool v)
{
    if (v == *value) return;

    *value = v;
    emitDataSourceUpdate();
}

void BoolDataSource::toggle() { set(!*value); }
